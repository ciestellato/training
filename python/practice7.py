"""
📝 演習問題（ファイル名：practice7.py）
辞書 scores = {"国語": 80, "数学": 90, "英語": 75} を使って、各科目の得点を表示するコードを書いてください。
"""

scores = {"国語": 80, "数学": 90, "英語": 75}
for subject, score in scores.items():
    print(f"{subject}:{score}点")

"""
💡 ポイント解説🔗
辞書は「キーと値」のペアで構成される。
.items() を使うとキーと値を同時に取り出せる。
キーは一意で、値は任意の型が使える。
要素の追加・更新・削除が可能。
"""