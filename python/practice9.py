"""
📝 演習問題（ファイル名：practice9.py）
辞書 scores = {"国語": 80, "数学": 90} に「英語: 85」を追加し、
「国語」の得点を85に更新、さらに「数学」を削除して、キーと値の一覧を表示するコードを書いてください。
"""

scores = {"国語": 80, "数学": 90}
scores["英語"] = 85
scores["国語"] = 85
del scores["数学"]
print(f"{scores}")

"""
💡 ポイント解説🔗
- dict[key] = value：追加または更新
- del dict[key]：指定キーの削除
- .keys() / .values()：キー・値の一覧取得（イテラブル）
- 辞書は「意味のあるデータ構造」に向いており、検索・更新が高速
"""